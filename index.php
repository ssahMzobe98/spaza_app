    function siyangenaManje(){
        const email=$(".email").val();
        const pass=$(".pass").val();
        $(".email").removeAttr("style");
        $(".pass").removeAttr("style");
        $(".processing").removeAttr("hidden");
        $(".processing").attr("style","width:100%;border:1px solid white;padding:5px 5px;").html("<img src='img/loader.gif' style='width:15%;border-radius: 50px;'> <span style='color:#45f3ff;'>Processing your request...</span> ");

