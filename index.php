            $(".email").attr("style","border:2px solid red");
            $(".processing").attr("style","padding:5px 5px;color:red;").html("Email Input Required**");

        }
        else if(pass.length<1){
            $(".pass").attr("style","border:2px solid red");
            $(".processing").attr("style","padding:5px 5px;color:red;").html("Password Input Required**");
        }
        else if(!ValidateEmail(email)){
            $(".email").attr("style","border:2px solid red");
            $(".processing").attr("style","padding:5px 5px;color:red;").html("Email NOT Valid**");
        }
        else{
            $.ajax({
                url:'./controller/loginProcessor.php',
                type:'post',
                data:{email:email,pass:pass},
                success:function(e){
                    console.log(e);
                    if(e.length>1){
                        $(".processing").attr("style","padding:5px 5px;color:red;text-align:center;").html(e);
